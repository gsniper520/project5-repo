// <!-- 1st card-->
function handleClickButton (){
    // <!-- 1st card text section-->
    const selectedItemsContainer = document.getElementById('selected-items');
    const handleClickId = document.getElementById('handle-text-id1');
    const handleClickText = handleClickId.innerText;
    const li = document.createElement("li");
    li.innerText = handleClickText;
    selectedItemsContainer.appendChild(li);

    // <!-- 1st card price section-->
    const handlePriceId = document.getElementById('handle-price-id1');
    const handelPriceText = handlePriceId.innerText;
    console.log(handelPriceText);
}

